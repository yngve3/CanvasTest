package com.example.canvastest.Shapes;

public class Line {

    public float startX;
    public float startY;
    public float stopX;
    public float stopY;

    public Line() {}

    public Line(float startX, float startY, float stopX, float stopY) {
        this.startX = startX;
        this.startY = startY;
        this.stopX = stopX;
        this.stopY = stopY;
    }
}
